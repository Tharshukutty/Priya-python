# geometry

def add(x,y):
    return x+y

def sub(x,y):
    return x-y

def mul(x,y):
    return x*y

def div(x,y):
    return x/y

def double(x):
    return x*2


import math

def circle_area(radius):
    return math.pi* (radius**2)

def rectangle_area(length,width):
    return length*width

def square_area(side):
    return side**2

def triangle_area(base,height):
    return base*height